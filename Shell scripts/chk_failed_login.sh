#!/bin/bash
# 获取登录失败的IP和失败次数

grep -i "Failed password for" /var/log/secure |egrep -o "(25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}|[1-9]?[0-9])\.(25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}|[1-9]?[0-9])\.(25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}|[1-9]?[0-9])\.(25[0-5]|2[0-4][0-9]|[0-1][0-9]{2}|[1-9]?[0-9])" | sort | uniq -c | sort -nr
